"""
示例 1：用最简单的方式理解 `yield` 和生成器。

学习生成器时，最重要的不是先记语法，
而是理解它的执行模型：

1. 普通函数
   调用后会一路执行到结束，然后一次性返回结果。
2. 含有 `yield` 的函数
   调用后不会立刻把所有代码跑完，而是返回一个“生成器对象”。
   之后每次通过 `next(...)` 索要数据时，函数才会继续向下执行，
   执行到下一个 `yield` 再暂停。

你可以把生成器想成：
“一个能暂停、能继续、能一点点交付结果的函数。”

这和后面项目里的 SSE 流式返回非常像：
- 不是一次性把所有内容准备好
- 而是边生成、边往外发送
"""


def simple_generator():
    """
    一个最简单的生成器函数。

    每次执行到 `yield` 时：
    - 把一个值交给外部
    - 自己在当前位置暂停

    下次再调用 `next(...)` 时，
    会从上一次暂停的位置继续执行。
    """

    print("--- 准备生成第 1 个数据 ---")
    yield 1

    print("--- 准备生成第 2 个数据 ---")
    yield 2

    print("--- 准备生成第 3 个数据 ---")
    yield 3


def abc():
    """
    一个普通函数，用来和生成器做对比。

    它不会暂停，也不会分阶段返回，
    而是直接算完后一次性返回最终结果。
    """

    return 1 + 1


# 1. 创建生成器对象。
#
# 注意：
# 调用 `simple_generator()` 的这一刻，
# 函数内部代码一行都还没有真正执行。
#
# 它只是返回了一个“可迭代、可逐步产出数据”的生成器对象。
gen = simple_generator()


# 2. 手动索要数据。
#
# 第一次 `next(gen)`：
# - 函数开始执行
# - 打印“准备生成第 1 个数据”
# - 执行到 `yield 1`
# - 把 1 返回给外部
# - 在这一行暂停
print(f"拿到数据: {next(gen)}")


# 第二次 `next(gen)`：
# - 从上次暂停的位置继续
# - 执行到 `yield 2`
# - 返回 2
# - 再次暂停
print(f"拿到数据: {next(gen)}")


# 第三次 `next(gen)`：
# - 继续执行到 `yield 3`
# - 返回 3
print(f"拿到数据: {next(gen)}")


# 第四次 `next(gen)`：
# - 函数已经没有更多 `yield`
# - 会抛出 `StopIteration`
#
# 这一行保留原始示例逻辑，目的是让你看到：
# “生成器的数据是有限的，取完就结束。”
print(f"拿到数据: {next(gen)}")


