"""
`02_tools` 这一章主要讲“大模型工具调用（Tool Calling / Function Calling）”。

学习重点：

1. 什么是工具
   工具本质上是“模型可以请求程序代为执行的函数”。

2. 为什么需要工具
   模型擅长理解和生成文本，但它本身并不能直接访问你本地的数据库、
   天气服务、订单系统、文件系统等真实能力。
   工具调用机制的作用，就是让模型把“我想执行什么操作”表达出来，
   再由你的 Python 代码真正去执行。

3. 一个完整工具调用流程包含什么
   - 先把工具的名称、用途、参数结构告诉模型
   - 模型判断当前问题是否需要调用工具
   - 如果需要，模型返回工具名和参数
   - 你的代码执行真实函数
   - 把函数结果回传给模型
   - 模型基于工具结果生成最终自然语言回复
"""
